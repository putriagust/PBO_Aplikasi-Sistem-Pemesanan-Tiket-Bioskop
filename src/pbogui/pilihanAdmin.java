        // TODO add your handling code here:
        String judul = judulnya.getText();
        String id = idnya.getText();
        String jam = jamnya.getText();
        String gen = gennya.getText();
        String stu = String.valueOf(stdnya.getSelectedItem());
        String hrg = harganya.getText();
        String sql;
        
        if(judul.isEmpty() || id.isEmpty() || jam.isEmpty() || gen.isEmpty() || stu.isEmpty() || hrg.isEmpty())
            JOptionPane.showMessageDialog(null, "Harap isi semua form");
        else {
            try {
                sql="insert into movie values('"+id+"','"+judul+"','"+jam+"','"+gen+"','"+stu+"','"+hrg+"')";
                PreparedStatement ps = kon.prepareStatement(sql);
                ps.executeUpdate(sql);

                JOptionPane.showMessageDialog(null, "Film berhasil ditambahkan" );
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
